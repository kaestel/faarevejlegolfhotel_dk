	<div id="oeer">

		<h2>Øer</h2>

		<h3>Sejer&oslash;</h3>
		<p>
			Sejer&oslash;, der er 11 km lang og 2 km bred &ndash; er et af de steder i Danmark, der er 
			begunstiget med flest solskinstimer. Det milde klima, det kuperede landskab og de mange 
			sm&aring; badestrande g&oslash;r &oslash;en til et popul&aelig;rt udflugtsm&aring;l.
		</p>
		<p>
			&Oslash;en har flere gravh&oslash;je og en bautasten (nordvest for Sejerby). Kirken er 
			bygget i slutningen af 1200 &ndash; tallet, og rummer smukke kalkmalerier.
		</p>
		<p>
			Turen til Sejer&oslash; foreg&aring;r med f&aelig;rge fra Havns&oslash;, hvorfra 
			ogs&aring; f&aelig;rgen til Neksel&oslash; udg&aring;r.
		</p>
		<ul class="links">
			<li><a href="http://www.sejero.dk">http://www.sejero.dk</a></li>
		</ul>

		<h3>Neksel&oslash;</h3>
		<p>
			Neksel&oslash;, 4 km lang og 1 km bred &ndash; har ingen overnatningsmuligheder, 
			men &oslash;ens kuperede landskab og enest&aring;ende natur g&oslash;r den til et 
			oplagt m&aring;l for en dagstur.
		</p>
		<p>
			Der er to afm&aelig;rkede vandreruter p&aring; &oslash;en. Undervejs kan man observere 
			et enest&aring;ende fugleliv.
		</p>
		<ul class="links">
			<li><a href="http://www.nexeloe.dk">http://www.nexeloe.dk</a></li>
		</ul>


	<h1>Lokalomr&aring;det</h1>

	<p>
		Hotellet ligger for foden af Vejrh&oslash;j, i det natursk&oslash;nne 
		Odsherred, på bredden af den inddæmmede lammefjord og lokalområdet
		byder på en bred vifte af natur- og kulturoplevelser, både fra nutiden og fortiden.
	</p>
	<p>
		Odsherred - Tid til livet. S&aring;dan lyder &quot;varedeklarationen&quot; for det nordvestlige 
		hj&oslash;rne af Sj&aelig;lland. Og i Odsherred er der masser af bruge tiden på.
	</p>


	<div id="lystfiskeri">

		<h2>Lystfiskeri</h2>
		<p>
			Med en kystlinie p&aring; knap 150 km stikkende mod samtlige 4 verdenshj&oslash;rner har 
			Odsherred - uanset vindretningen - altid et stykke kyst, der indbyder til fiskeri. Kystlinien 
			veksler fra stejle klinter til flade sandstrande, og har masser at tilbyde s&aring;vel nybegyndere 
			som de mere garvede. Ved Odsherreds kyster fanges prim&aelig;rt hav&oslash;rred, fladfisk og hornfisk.
		</p>
		<p>
			Har I lyst til en havfisketur med kutter, er der ogs&aring; mulighed for dette. Fra 
			Havns&oslash;, Sj. Odde og R&oslash;rvig styrer de lokalkendte skippere ud mod de gode 
			fiskepladser. Vil I selv en tur p&aring; havet, er der mulighed for at leje en jolle med 
			p&aring;h&aelig;ngsmotor. B&aring;dene leveres direkte til vandkanten. P&aring; havet fanges 
			mest torsk, makrel og fladfisk.
		</p>
		<p>
			Er I til fiskeri i mere rolige omgivelser, er Put and take s&oslash;erne en god mulighed. 
			Her er I n&aelig;sten sikre p&aring; at aftensmaden er hjemme. I s&oslash;erne kan I 
			udover &oslash;rreder ogs&aring; fange geder og aborrer.
		</p>
		<p>
			I Lammefjordskanalerne kan man pr&oslash;ve det helt specielle fiskeri af de sky karper.
		</p>


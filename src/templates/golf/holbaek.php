	<h1>Holb&aelig;k Golfklub</h1>
	<p>
		Klubben blev startet i 1964 som en 9-hullers bane p&aring; en holm &ndash; 
		Kirseb&aelig;rholmen - i Holb&aelig;k Fjord. Arealet var et gammelt 
		skydeterr&aelig;n, der bortset fra granatnedslag var uber&oslash;rt og jomfrueligt.
	</p>
	<p>
		Banen blev tegnet af Frederik Dreyer og var i princippet en sea-side bane, hvor kun 
		strandvolde beskyttede bane og fairways mod fjorden. Beliggenheden er unik, og 
		Kirseb&aelig;rholmen er et af de f&aring; steder i Danmark, hvor man stadig kan finde 
		uber&oslash;rte strandenge samt et rigt og varieret fugleliv.
	</p>
	<p>
		I 1993-94 blev banen udvidet til 18 huller. De sidste 9 huller blev anlagt med en 
		flot udsigt over Kirseb&aelig;rholmen, fjorden og Tuse N&aelig;s. Hullerne er 
		tegnet af Jan Sederholm. Alle huller, p&aring; n&aelig;r hul 2, er beliggende inden 
		for strandbeskyttelseslinien. Naturv&aelig;rdierne er store og underkastet myndighedernes 
		konstante overv&aring;gning. Rough-arealerne er visse steder af en karakter, der ikke er 
		egnet til golfspil. Til geng&aelig;ld er de placeret p&aring; omr&aring;der, hvor kun 
		et d&aring;rligt slag ender, og derfor er de normalt helt uden for spil. Dyrelivet har 
		her kronede dage og trives tilsyneladende i sk&oslash;n harmoni med det legende menneske.
	</p>
	<p>
		Banen er relativt kort. Den er rimelig flad, men ofte anstrengende p&aring; grund af den 
		herskende nordvestenvind ind over fjorden. Der er mange s&oslash;er og bunkers, og banen 
		kr&aelig;ver solide og pr&aelig;cise slag. Greens er flotte, middelhurtige og tager normalt 
		godt imod. Banen er trods sin manglende l&aelig;ngde udfordrende ogs&aring; for lavhandicappere, 
		og under eliteturneringer bliver par kun brudt af top 5.
	</p>


	<h1>Kalundborg Golfklub</h1>
	<p>Kalundborg Golfklub blev stiftet den 14. marts 1974 som klub nr. 25 indenfor Dansk Golf Union.</p>
	<p>
		I Kalundborg Golfklub finder du bl.a. en 18-Hullers golfbane par 70 - CR H/D 70,2/70,8 Slope 127/124.
	</p>
	<p>
		Derudover har klubben rt tr&aelig;ningsanl&aelig;g med driving range og Indspilsgreen med &oslash;vebunker.
		Putting green med 18 huller, Par 3 bane med 6 huller.
	</p>
	<p>
		Klubhus med omkl&aelig;dningsrum med badefaciliteter og kantine. Bagrum med udlejningsskabe til 
		opbevaring af medlemmernes golfudstyr.
	</p>

